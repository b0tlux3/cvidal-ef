package ef.modelo;

public class Revisiones {

    String nro_revision, cambio_aceite, cambio_filtro, revision_frenos, otros, nro_placa;

    public String getNro_revision() {
        return nro_revision;
    }

    public void setNro_revision(String nro_revision) {
        this.nro_revision = nro_revision;
    }

    public String getCambio_aceite() {
        return cambio_aceite;
    }

    public void setCambio_aceite(String cambio_aceite) {
        this.cambio_aceite = cambio_aceite;
    }

    public String getCambio_filtro() {
        return cambio_filtro;
    }

    public void setCambio_filtro(String cambio_filtro) {
        this.cambio_filtro = cambio_filtro;
    }

    public String getRevision_frenos() {
        return revision_frenos;
    }

    public void setRevision_frenos(String revision_frenos) {
        this.revision_frenos = revision_frenos;
    }

    public String getOtros() {
        return otros;
    }

    public void setOtros(String otros) {
        this.otros = otros;
    }

    public String getNro_placa() {
        return nro_placa;
    }

    public void setNro_placa(String nro_placa) {
        this.nro_placa = nro_placa;
    }

}
